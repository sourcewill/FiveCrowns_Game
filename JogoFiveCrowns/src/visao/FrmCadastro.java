    private void lblSalvarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblSalvarMouseReleased
        String nome = txtNome.getText();
        if (nome.equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um nome para o jogador!", "Campo obrigat√≥rio", 0);
        } else {
            JogadorDAO jDao = new JogadorDAO();
            jDao.inserir(nome);
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!", "Sucesso", 1);
            dispose();
        }
    }//GEN-LAST:event_lblSalvarMouseReleased

