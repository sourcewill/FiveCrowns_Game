    private void lblAdicionarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAdicionarMouseReleased

        Integer linha = tblPontuacao.getSelectedRow();
        Integer pontos;
        try {
            pontos = Integer.parseInt(txtCampo.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Digite números no campo de pontos!", "Erro", 0);
            return;
        }

        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um jogador para atribuir pontos!", "Nenhum jogador selecionado", 0);
        } else {
            Integer id = (Integer) tblPontuacao.getValueAt(linha, 0);
            JogadorDAO jDAO = new JogadorDAO();
            Jogador jogador = jDAO.getJogadorPorId(id);
            jogador.setPontos(jogador.getPontos() + pontos);
            preencheTabela();
            txtCampo.setText("");
        }
    }//GEN-LAST:event_lblAdicionarMouseReleased

    private void lblFinalizarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblFinalizarMouseReleased
        int opcao = JOptionPane.showConfirmDialog(null, "Finalizar rodada " + Dados.numRodada + " ?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if (opcao == 0) {
            Dados.numRodada++;
            lblNumRodada.setText(Dados.numRodada.toString());
        }
    }//GEN-LAST:event_lblFinalizarMouseReleased

    private void lblRemoverMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblRemoverMouseReleased
        Integer linha = tblPontuacao.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Nenhum jogador selecionado!", "Erro", 0);
        } else {
            int opcao = JOptionPane.showConfirmDialog(null, "Confirma a exclusão do jogador?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (opcao == 0) {
                Integer id = (Integer) tblPontuacao.getValueAt(linha, 0);
                JogadorDAO jDAO = new JogadorDAO();
                jDAO.remover(id);
                preencheTabela();
            }
        }
    }//GEN-LAST:event_lblRemoverMouseReleased

    private void lblEditarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEditarMouseReleased
        Integer linha = tblPontuacao.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Nenhum jogador selecionado!", "Erro", 0);
        } else {
            Integer id = (Integer) tblPontuacao.getValueAt(linha, 0);
            String nome = (String) tblPontuacao.getValueAt(linha, 1);
            Integer pontos = (Integer) tblPontuacao.getValueAt(linha, 2);
            FrmEditarJogador formEditar = new FrmEditarJogador(id, nome, pontos);
            formEditar.setLocationRelativeTo(this);
            formEditar.setVisible(true);
        }

    }//GEN-LAST:event_lblEditarMouseReleased

