    private void preencheTabela() {
        Collections.sort(Dados.listaDeJogadores);
        DefaultTableModel modelo = (DefaultTableModel) tblPontuacao.getModel();
        modelo.setNumRows(0);
        for (Jogador jogador : Dados.listaDeJogadores) {
            modelo.addRow(new Object[]{
                jogador.getId(),
                jogador.getNome(),
                jogador.getPontos()
            });
        }
    }

